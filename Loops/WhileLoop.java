package Loops;

// A simple program to demonstrate the use of while loop in Java
public class WhileLoop {
    public static void main(String[] args) {
        // A simple program to print numbers from 1 to 100 using a while loop
        int i = 1;
        while (i <= 100){
            System.out.println("Hello World " + i);
            i++;
        }
    }
}
