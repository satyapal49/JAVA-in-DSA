public class ArithematicOperators {  // Class declaration
    public static void main(String[] args) {  // Main method - entry point of the program
        int a = 5;  // Declare and initialize variable a
        int b = 10;  // Declare and initialize variable b
        System.out.println("Addition: " + (a + b)); // Addition
        System.out.println("Subtraction: " + (a - b)); // Subtraction
        System.out.println("Multiplication: " + (a * b)); // Multiplication
        System.out.println("Division: " + (a / b));  // Division
        System.out.println("Modulus: " + (a % b));  // Modulus ( remainder of division)
    }  // End of main method
}  // End of class ArithematicOperators