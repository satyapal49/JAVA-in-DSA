public class ArithematicOperators {  // Class declaration
    public static void main(String[] args) {  // Main method - entry point of the program
        int a = 5;  // Declare and initialize variable a
        int b = 10;  // Declare and initialize variable b

        // Binary operators perform operations on two operands. Here we will demonstrate the use of basic arithmetic operators.
        System.out.println("Addition: " + (a + b)); // Addition
        System.out.println("Subtraction: " + (a - b)); // Subtraction
        System.out.println("Multiplication: " + (a * b)); // Multiplication
        System.out.println("Division: " + (a / b));  // Division
        System.out.println("Modulus: " + (a % b));  // Modulus ( remainder of division)
    }  // End of main method
}  // End of class ArithematicOperators


// This program demonstrates the use of basic arithmetic operators in Java. It performs addition, subtraction, multiplication, division, and modulus operations on two integer variables a and b, and prints the results to the console.
// This is Binary operators, as they operate on two operands (a and b).
// The output of the program will be:
// Addition: 15
// Subtraction: -5
// Multiplication: 50
// Division: 0
// Modulus: 5
